package validator.OSLO2;

public class ValidatorFiles {
	private String data;
	private String shapes;
	
	public ValidatorFiles() {
		 
	   }
	 
	   public ValidatorFiles(String data, String shapes) {
	       this.data = data;
	       this.shapes = shapes;
	   }
	 
	   public String getdata() {
	       return data;
	   }
	 
	   public void setdata(String data) {
	       this.data = data;
	   }
	 
	   public String getshapes() {
	       return shapes;
	   }
	 
	   public void setshapes(String shapes) {
	       this.shapes = shapes;
	   }
	 
	}